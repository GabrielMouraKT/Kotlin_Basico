fun main(){

    /*
     Operadores Aritméticos
    + Operador de Soma
    - Operador de Subtração
    / Operador de Divisão
    * Operador de Multiplicação
    % Modulo - Resto da Divisão
    ++ Incremento
    -- Decremento
    */

    var numero1 = 20
    var numero2 = 10

    println(numero1 + numero2)
    println(numero1 - numero2)
    println(numero1 * numero2)
    println(numero1 / numero2)
    println(numero1 % numero2)
    println(numero1 ++)
    println(numero1 --)





}


