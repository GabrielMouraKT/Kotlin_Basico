fun main(){

   /*
   Operadores Relacionais
   == Operador de Igual
   <  Operador Menor
   >  Operador Maior
   <= Operador Menor ou Igual
   >= Operador Maior ou Igual
   != Operador de Diferente

    */

    var valor1 = 20
    var valor2 = 10

    println(valor1 == valor2)
    println(valor1 != valor2)
    println(valor1 < valor2)
    println(valor1 > valor2)
    println(valor1 <= valor2)
    println(valor1 >= valor2)

}